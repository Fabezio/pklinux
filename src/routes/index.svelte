    <p>Certes, Windows occupe une grande place dans nos vies, c'est à l'heure actuelle le système d'exploitation majeur, celui qui est incontournable. Pourquoi? parce que tout le monde connait. vous rencontrerez rarement quelqu'un qui n'en a jamais entendu parler. En bref, <b>presque tout le monde a un ordinateur fonctionnnant sous Windows</b>, renforçant l'idée préconçue que c'est <b>le </b>système de référence. Et pourtant...</p>
    <p>Pourtant, en dépit de tous ses avantages et son joli design, l'OS de <b>Microsoft Systems </b>est loin d'être parfait. Même très loin.
      Seulement, il est pratique, son usage est répandu, y compris dans le milieu professionnel (grâce à la distribution pro, plus stable que l'édition publique et dont les outils bureautiques sont compris dans cette mouture).
      Il y a mieux. Je ne citerai pas MacOS <em>(trop tard!) </em>, le système d'Apple, bien plus stable, au matériel robuste (n'allez pas le claquer contre un mur, il y a des limites), mais dont la plupart des outils sont payants. Quant au dépot, tout comme son rival, il n'est pas intégré par défaut et son usage est
      <q>limité aux utilisateurs avertis</q>. Alors, qu'est-ce que ces deux systèmes d'expmloiation ont en commun? D'abord il sont répandus, leur usage est plutôt simple (un petit didacticiel est disponible au premier démarrage pour les débutants). Et ils sont payants. MacOS est d'au moins le double de Windows version familiale. En outre, si vous ne savez pas où chercher, vous tomberez souvent sur des logiciels propriétaires donc (pareil) payants ou en version freemium (gratuits de base comme une version d'évaluation, mais moyennant finance pour une version intégrale). Et il n'est même pas garanti qu'au final un malware, virus ou autre script malveillant n'a pas été glissé dans le programme. Reconnaissez que c'est cher payé dans un tel cas de figure.
    </p>
    <p>En clair ce n'est pas des plus pratiques et économiques si votre bourse est limitée. Le mieux est encore de trouver un OS alliant stabilité, accessibilité, et des plus fournis dès le premier démarrage. </p>
    <p>Linux est fait pour ça.</p>
    <h2>Qu'est-ce que Linux</h2>
    <p>GNU/Linux de son vrai nom est un système d'exploitation très complet, qui se décline en de multiples versions et distributions (je vous épargne les détatils dans cette section). Son logo est un manchot pygmée nommé TUX (<b>non, Tux n'est pas un pingouin!</b>). Une liste des distributions majeures est disponible
      <a href="distros">ci-après</a>. La seule participation financière est le support sur lequel vous allez enregistrer le programme d'installation au format <em>iso </em>. L'installation est le plus souvent facile et automatisée. Je vous conseillerai vivement de suivre un tutoriel en ligne avant de commencer son utitisation. Pour finir, je vous dirai que vous avez l'embarras du choix et que vous pouvez parfaitement remplacer votre linux, voire supprimer sa partition s'il ne vous convient pas ou ne correspond pas à votre usage (ce qui est rare).
