<h1 class="">
  Les Environnements de Bureau ou Environment Desktops
  <br />(dits aussi
  <em> spins </em>ou
  <em>saveurs</em>)
</h1>
<p>
  Sur Linux il y a plein d'environnements de bureau, Gnome et KDE étant les plus
  utilisés. Bien sûr il en existe bien d'autres, tels XFCE, XLDE, LXQT, Mate,
  Cinnamon, Budgie, Deepin. Et je n'ai cité que les plus connus.
  <br />
  Certains d'entre eux sont issus d'une distribution et ajoutés à d'autres. La
  plupart sont installables directement, figurant dans les dépôts mirroirs. Tous
  ont leur code source téléchargeable leur propre site web ou un depot Github,
  site hébergeant (justement) des codes sources.
</p>
<h2>C'est quoi un environnement de bureau?</h2>
<p>
  Un DE est un logiciel permettant d'établir une interface graphique, sous forme
  de disposition de fenetres, avec un tableau de bord (barre des taches linux)
  <br />Leur variété resulte de besoins différents en fonction des taches à
  accomplir ou des habitudes. Pour exemple si voius venez de windows, vous serez
  plus à l'aise avec Cinnamon ou KDE. En revanche si votre OS précédent est Mac,
  il vous faudra plutot Budgie ou Deepin. Si vous êtes un inconditionnel de
  Android, C'est Gnome qui s'imposera.
</p>
